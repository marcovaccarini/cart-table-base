     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $cart_id = $request->cookie('cart_id');
        $wishes = Wish::where('cart_id', '=', $cart_id)
            ->with('product')
            ->with('ProductImages')
            ->orderBy('updated_at', 'desc')
            ->get();

        return view('wishlist.show', compact('wishes'));
    }

